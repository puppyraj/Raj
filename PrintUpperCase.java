public class PrintUpperCase {
	public static void main(String[]args) {
		String word = "OneSoft";
		String value = word.toUpperCase();
		System.out.println(value);
		String value1 = "OneSoft".toUpperCase();
		System.out.println(value1);
		System.out.println("OneSoft".toUpperCase());
		
		
	}

}
