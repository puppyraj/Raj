package Day3;

public class PrintLowerCase {
	public static void main(String[]args) {
		String word = "ONESOFT";
		String value = word.toLowerCase();
		System.out.println(value);
		String value1 = "ONESOFT".toLowerCase();
		System.out.println(value1);
		System.out.println("ONESOFT".toLowerCase());
		
		
	}

}
